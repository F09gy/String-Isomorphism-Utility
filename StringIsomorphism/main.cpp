#include "StringIsomorphism.h"
#include <iostream>
#include <string>

int main(int argc, char* argv[])
{
    // Проверить количество переданных аргументов командной строки
    // Если количество аргументов не равно двум (не считая имени программы)
    if (argc != 3)
    {
        // Вывести в консоль сообщение об ошибке неверного количества аргументов
        std::cerr << "Ошибка ввода: аргументов меньше или больше, чем 2." << std::endl;
        // Завершить программу с кодом ошибки
        return static_cast<int>(ErrorCode::ERR_ARGS_COUNT);
    }

    // Сохранить пути к входному и выходному файлу из аргументов командной строки
    std::string inputPath = argv[1];
    std::string outputPath = argv[2];
    std::vector<std::string> lines;
    
    // Прочитать содержимое входного файла построчно (readInputFile)
    // Если функция считывания входного файла (readInputFile) вернула ложь
    if (!readInputFile(inputPath, lines))
    {
        // Вывести в консоль сообщение “Ошибка чтения файла”
        std::cerr << "Ошибка чтения файла: " << inputPath << "\n";
        // Завершить программу с кодом ошибки
        return static_cast<int>(ErrorCode::ERR_READ);
    }

    // Проверить количество прочитанных строк (lines.size())
    // Если строк меньше двух
    if (lines.size() < 2)
    {
        // Вывести в консоль сообщение “Содержание указанного файла меньше двух строк”
        std::cerr << "Содержание указанного файла меньше двух строк\n";
        // Завершить программу с кодом ошибки
        return static_cast<int>(ErrorCode::ERR_LINE_LENGTH);
    }

    // Если строк больше двух
    if (lines.size() > 2)
    {
        // Вывести в консоль сообщение “Содержание указанного файла больше двух строк”
        std::cerr << "Содержание указанного файла больше двух строк\n";
        // Завершить программу с кодом ошибки
        return static_cast<int>(ErrorCode::ERR_LINE_LENGTH);
    }

    // Получить две строки (из вектора lines)
    std::string str1 = lines[0];
    std::string str2 = lines[1];

    // Проверить длину первой строки
    // Если длина меньше 1 или больше 100
    if (str1.length() < 1 || str1.length() > 100)
    {
        // Вывести в консоль сообщение о недопустимой длине строки
        std::cerr << "В одной из строк количество символов выходит за границы допустимого\n";
        // Завершить программу с кодом ошибки
        return static_cast<int>(ErrorCode::ERR_LINE_LENGTH);
    }

    // Проверить длину второй строки
    // Если длина меньше 1 или больше 100
    if (str2.length() < 1 || str2.length() > 100)
    {
        // Вывести в консоль сообщение о недопустимой длине строки
        std::cerr << "В одной из строк количество символов выходит за границы допустимого\n";
        // Завершить программу с кодом ошибки
        return static_cast<int>(ErrorCode::ERR_LINE_LENGTH);
    }

    // Проверить символы первой строки
    // Для каждого символа первой строки
    for (char c : str1)
    {
        // Если символ не является латинской буквой, цифрой, точкой или запятой
        if (!(std::isalpha(static_cast<unsigned char>(c)) ||
            std::isdigit(static_cast<unsigned char>(c)) ||
            c == '.' ||
            c == ','))
        {
            // Вывести в консоль сообщение о запрещённых символах
            std::cerr << "Содержание указанного файла имеет запрещённые символы\n";
            // Завершить программу с кодом ошибки
            return static_cast<int>(ErrorCode::ERR_INVALID_CHARS);
        }
    }

    // Проверить символы второй строки
    // Для каждого символа второй строки
    for (char c : str2)
    {
        // Если символ не является латинской буквой, цифрой, точкой или запятой
        if (!(std::isalpha(static_cast<unsigned char>(c)) ||
            std::isdigit(static_cast<unsigned char>(c)) ||
            c == '.' ||
            c == ','))
        {
            // Вывести в консоль сообщение о запрещённых символах
            std::cerr << "Содержание указанного файла имеет запрещённые символы\n";
            // Завершить программу с кодом ошибки
            return static_cast<int>(ErrorCode::ERR_INVALID_CHARS);
        }
    }

    // Создать пустой словарь соответствий
    std::map<char, char> mapping;

    // Вызвать функцию проверки изоморфности (solveIsomorphism) и передать словарь по ссылке
    bool isIsomorphic = solveIsomorphism(str1, str2, mapping);

    // Если функция вернула “Изоморфны”
    if (isIsomorphic)
    {
        // Вызвать функцию записи результата в файл (writeOutputFile), 
        // передав путь к выходному файлу, флаг изоморфности и заполненный словарь (mapping)
        // Если функция записи результата вернула ложь
        if (!writeOutputFile(outputPath, isIsomorphic, mapping))
        {
            // Вывести в консоль сообщение “Не получилось создать или открыть выходной файл”
            std::cerr << "Не получилось создать или открыть выходной файл\n";
            // Завершить программу с кодом ошибки
            return static_cast<int>(ErrorCode::ERR_OUTPUT_FILE_CREATE);
        }
    }
    // Если функция “Не изоморфны”
    else
    {
        // Вызвать функцию записи результата в файл (writeOutputFile), 
        // передав путь к выходному файлу, флаг не изоморфности и пустой словарь (mapping)
        // Если функция записи результата в файл (writeOutputFile) вернула ложь
        if (!writeOutputFile(outputPath, isIsomorphic, mapping))
        {
            // Вывести в консоль сообщение “Не получилось создать или записать выходной файл”
            std::cerr << "Не получилось создать или записать выходной файл\n";
            // Завершить программу с кодом ошибки
            return static_cast<int>(ErrorCode::ERR_OUTPUT_FILE_CREATE);
        }
    }

    // Успешно завершить программу
    return static_cast<int>(ErrorCode::SUCCESS);
}